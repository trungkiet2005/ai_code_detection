def _generate_hard_negative(code: str) -> str:
    """Generate hard negative (semantically equivalent, stylistically different)."""
    # Rename identifiers
    pattern = r'\b([a-zA-Z_][a-zA-Z0-9_]*)\b'
    var_map = {}
    counter = 0

    def replacer(m):
        nonlocal counter
        name = m.group(1)
        if name not in var_map:
            var_map[name] = f"v{counter}"
            counter += 1
        return var_map[name]

    refactored = re.sub(pattern, replacer, code)
    return refactored


def _contrastive_hard_negatives_score(codes: List[str], cfg: ZSConfig) -> np.ndarray:
    """Style distance via contrastive hard negatives."""
    import torch
    from transformers import AutoModelForMaskedLM, AutoTokenizer

    logger.info(f"[ZS-22] Loading encoder {cfg.scorer_lm}...")
    tokenizer = AutoTokenizer.from_pretrained(cfg.scorer_lm)
    model = AutoModelForMaskedLM.from_pretrained(cfg.scorer_lm)
    model.eval()
    if cfg.device == "cuda" and torch.cuda.is_available():
        model = model.to("cuda")
        if cfg.precision == "bf16":
            model = model.to(torch.bfloat16)

    scores = np.zeros(len(codes), dtype=np.float64)
    bs = cfg.batch_size

    with torch.no_grad():
        for start in range(0, len(codes), bs):
            chunk = codes[start : start + bs]

            # Encode originals
            enc_orig = tokenizer(
                chunk, max_length=cfg.scorer_max_length,
                padding="max_length", truncation=True, return_tensors="pt",
            )
            input_ids = enc_orig["input_ids"]
            attn = enc_orig["attention_mask"]
            if cfg.device == "cuda":
                input_ids = input_ids.to("cuda")
                attn = attn.to("cuda")

            out_orig = model(input_ids=input_ids, attention_mask=attn).logits.float()
            emb_orig = out_orig[:, 0, :].cpu()

            # Generate hard negatives
            hard_negs = [_generate_hard_negative(c) for c in chunk]

            # Encode hard negatives
            enc_neg = tokenizer(
                hard_negs, max_length=cfg.scorer_max_length,
                padding="max_length", truncation=True, return_tensors="pt",
            )
            input_ids_n = enc_neg["input_ids"]
            attn_n = enc_neg["attention_mask"]
            if cfg.device == "cuda":
                input_ids_n = input_ids_n.to("cuda")
                attn_n = attn_n.to("cuda")

            out_neg = model(input_ids=input_ids_n, attention_mask=attn_n).logits.float()
            emb_neg = out_neg[:, 0, :].cpu()

            # Score = L2 distance (style divergence)
            dists = torch.norm(emb_orig - emb_neg, dim=1).numpy()
            for i in range(len(chunk)):
                scores[start + i] = float(dists[i])

    return scores


