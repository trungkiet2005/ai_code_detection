"""
[exp_zs_22] ContrastiveHardNegatives -- zero-shot detector via manifold
            disentanglement: hard negatives force separation of style from
            content in embedding space.

THEORY HOOK (SOTA, ACL Findings 2025, arXiv:2411.18472):
  Content leakage is INEVITABLE in any authorship embedding space. Rather
  than fighting it, use synthetic HARD NEGATIVES (semantically similar but
  different-author code) to force the model to separate style subspace from
  content subspace.

  Key insight: If two code snippets are semantically equivalent but written
  by different authors, the embedding should separate them via STYLE signal.
  This is learnable via contrastive loss (InfoNCE) with hard negatives.

  For code authorship: hard negatives are generated by refactoring
  (variable rename, statement reorder, comment preservation). Same semantics,
  different syntactic style -> learns pure style signal.

WHY NOVEL:
  * Disentangles STYLE from CONTENT (orthogonal to all existing detectors).
  * For code, semantically-equivalent refactorings are easy to generate
    (unlike text where paraphrasing is hard).
  * Zero-shot: no fine-tuning. Computes style distance via embedding
    divergence from semantically-similar anchor.

IMPLEMENTATION (hard negatives via refactoring):
  1. Encode code with UniXcoder -> embedding e.
  2. Generate semantically-equivalent refactored version:
     - Rename all variables (change names, keep logic)
     - Add/remove comments (preserve meaning)
     - Reorder independent statements
  3. Encode refactored -> embedding e_refac.
  4. For detection: measure ||e - e_refac|| (style distance).
  5. Score = this distance. Higher = human-like (more style variation).

Cost: 2 forward passes per sample (original + refactored). ~13 min on H100.
"""
